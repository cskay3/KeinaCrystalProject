    //When both defect:
    //
    //* Each gets **P (Punishment)**.
    //* But each was trying to avoid getting **S (the worst outcome)** — being betrayed while cooperating.
    //
    //So even though mutual cooperation is better for both, **rational self-interest pushes both toward defection**, which leads to a worse collective result.
    //
    //---
    //
    //### Key Insight
    //
    //When both defect:
    //
    //* It is the **Nash equilibrium** (no one can improve by changing their choice alone).
